const googleProvider = new firebase.auth.GoogleAuthProvider();

firebase.auth().onAuthStateChanged((user) => {
  if (user) {
    showSignedIn(user.displayName || user.email);
  } else {
    showSignInButton();
  }
});

function showSignInButton() {
  const header = document.querySelector('header');
  if (document.getElementById('auth-container')) return;

  const authDiv = document.createElement('div');
  authDiv.id = "auth-container";
  authDiv.style.marginTop = "15px";
  authDiv.innerHTML = `
    <button id="googleSignInBtn" style="
      padding: 12px 24px;
      background: linear-gradient(135deg, #4285F4, #34A853);
      color: white;
      border: none;
      border-radius: 12px;
      font-size: 16px;
      font-weight: 600;
      cursor: pointer;
      box-shadow: 0 4px 12px rgba(66, 133, 244, 0.3);
    ">
      Sign in with Google ðŸš€
    </button>
  `;
  header.appendChild(authDiv);

  document.getElementById('googleSignInBtn').onclick = () => {
    firebase.auth().signInWithPopup(googleProvider)
      .catch((error) => {
        addMessage("Sign in failed: " + error.message, "bot");
      });
  };
}

