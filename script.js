function addMessage(content, type) {
  const div = document.createElement("div");
  div.className = `message ${type}`;

  // Handle string (text or single image URL/base64)
  if (typeof content === "string") {
    if (content.startsWith("http") || content.startsWith("data:image")) {
      // It's an image
      const img = document.createElement("img");
      img.src = content;
      img.alt = "Generated by Nova AI";
      img.loading = "lazy";
      img.style.maxWidth = "100%";
      img.style.borderRadius = "12px";
      img.style.marginTop = "10px";
      img.style.boxShadow = "0 4px 12px rgba(0,0,0,0.5)";
      div.appendChild(img);
    } else {
      // Normal text
      div.innerText = content;
    }
  }
  // Handle array (multiple images or text + images)
  else if (Array.isArray(content)) {
    content.forEach(part => {
      if (typeof part === "string") {
        if (part.startsWith("http") || part.startsWith("data:image")) {
          const img = document.createElement("img");
          img.src = part;
          img.alt = "Generated by Nova AI";
          img.loading = "lazy";
          img.style.maxWidth = "100%";
          img.style.borderRadius = "12px";
          img.style.margin = "10px 0";
          img.style.display = "block";
          div.appendChild(img);
        } else {
          div.appendChild(document.createTextNode(part));
          div.appendChild(document.createElement("br"));
        }
      }
    });
  } else {
    div.innerText = String(content);
  }

  chatArea.appendChild(div);
  chatArea.scrollTop = chatArea.scrollHeight;
}

sendBtn.onclick = sendMessage;
input.addEventListener("keydown", e => {
  if (e.key === "Enter") {
    e.preventDefault();
    sendMessage();
  }
});

async function sendMessage() {
  const text = input.value.trim();
  if (!text) return;

  addMessage(text, "user");
  input.value = "";

  messages.push({ role: "user", content: text });

  try {
    const res = await fetch("/api/chat", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ messages })
    });

    const data = await res.json();
    addMessage(data.reply, "bot");
    messages.push({ role: "assistant", content: data.reply });

  } catch (err) {
    console.error(err);
    addMessage("Error connecting to Nova AI.", "bot");
  }
}

