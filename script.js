function addMessage(content, type) {
  const div = document.createElement("div");
  div.className = `message ${type}`;

  if (content instanceof HTMLImageElement) {
    content.alt = "Generated by Nova AI with FLUX";
    content.loading = "lazy";
    content.style.maxWidth = "100%";
    content.style.borderRadius = "12px";
    content.style.marginTop = "10px";
    content.style.boxShadow = "0 4px 12px rgba(0,0,0,0.5)";
    div.appendChild(content);
  } else if (typeof content === "string") {
    if (content.startsWith("http") || content.startsWith("data:image")) {
      const img = document.createElement("img");
      img.src = content;
      img.alt = "Generated by Nova AI";
      img.loading = "lazy";
      img.style.maxWidth = "100%";
      img.style.borderRadius = "12px";
      img.style.marginTop = "10px";
      div.appendChild(img);
    } else {
      div.innerText = content;
    }
  } else if (Array.isArray(content)) {
    content.forEach(part => {
      if (typeof part === "string" && (part.startsWith("http") || part.startsWith("data:image"))) {
        const img = document.createElement("img");
        img.src = part;
        img.style.maxWidth = "100%";
        img.style.borderRadius = "12px";
        img.style.margin = "10px 0";
        img.style.display = "block";
        div.appendChild(img);
      } else {
        div.appendChild(document.createTextNode(part));
        div.appendChild(document.createElement("br"));
      }
    });
  }

  chatArea.appendChild(div);
  chatArea.scrollTop = chatArea.scrollHeight;
}

sendBtn.onclick = sendMessage;
input.addEventListener("keydown", e => {
  if (e.key === "Enter") {
    e.preventDefault();
    sendMessage();
  }
});

async function sendMessage() {
  const text = input.value.trim();
  if (!text) return;

  addMessage(text, "user");
  input.value = "";

  messages.push({ role: "user", content: text });

  const lowerText = text.toLowerCase();
  const isImageRequest = lowerText.includes("generate") || lowerText.includes("create") || 
                         lowerText.includes("draw") || lowerText.includes("image") || 
                         lowerText.includes("picture") || lowerText.includes("logo") ||
                         lowerText.includes("kingdom") || lowerText.includes("city") ||
                         lowerText.includes("futuristic") || lowerText.includes("cyberpunk");

  if (isImageRequest) {
    // Client-side real FLUX gen with Puter.js
    const loadingDiv = document.createElement("div");
    loadingDiv.className = "message bot";
    loadingDiv.innerText = "Generating with FLUX.1-schnell... ðŸ”¥ (5-20 sec)";
    chatArea.appendChild(loadingDiv);
    chatArea.scrollTop = chatArea.scrollHeight;

    try {
      const imageElement = await puter.ai.txt2img(text, {
        model: "black-forest-labs/FLUX.1-schnell"  // real fast high-quality FLUX
      });
      chatArea.removeChild(loadingDiv);
      addMessage(imageElement, "bot");
    } catch (err) {
      chatArea.removeChild(loadingDiv);
      addMessage("Image gen failed â€” try again or simpler prompt bro!", "bot");
    }
  } else {
    // Normal chat via backend
    try {
      const res = await fetch("/api/chat", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ messages })
      });

      const data = await res.json();
      addMessage(data.reply, "bot");
      messages.push({ role: "assistant", content: data.reply });

    } catch (err) {
      addMessage("Error connecting to Nova AI.", "bot");
    }
  }
}

